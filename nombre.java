public class nombre {
    String nombre;
    float salario;
    char sexo;
    String cargo;

    public nombre(String nombre, float salario, char sexo, String cargo) {
        this.nombre = nombre;
        this.salario = salario;
        this.sexo = sexo;
        this.cargo = cargo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public float getSalario() {
        return salario;
    }

    public void setSalario(float salario) {
        this.salario = salario;
    }

    public char getSexo() {
        return sexo;
    }

    public void setSexo(char sexo) {
        this.sexo = sexo;
    }

    public String getCargo() {
        return cargo;
    }

    public void setCargo(String cargo) {
        this.cargo = cargo;
    }

}
